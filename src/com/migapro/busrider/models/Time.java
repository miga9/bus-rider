package com.migapro.busrider.models;

public class Time {
	private String mHours;
	private String mMinutes;
	
	public String getHours() {
		return mHours;
	}
	
	public String getMinutes() {
		return mMinutes;
	}
	
	public void setHours(String hours) {
		mHours = hours;
	}
	
	public void setMinutes(String minutes) {
		mMinutes = minutes;
	}
	
}
